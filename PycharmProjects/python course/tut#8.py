print(mystr[0:5])
print(mystr[0:22:2])#this will print the full sentence by leaving 1 letter each
print (len(mystr))#len function is used to measure the length of the sentence
print(mystr.isalnum())#this means if the sentence will have spaces it will print false= alpha numeric
print(mystr.isalpha())
print(mystr.endswith("boy"))#it means from which word the sentence ends
print(mystr.count("o"))#it will count the letter which you had given it
print(mystr.capitalize())#it willmake your 1st letter capital
print(mystr.find("good"))#it wil find any word for you in the sentence
print(mystr.upper())#it will convert the full sentence in capital letters
print(mystr.lower())#it will convert the full sentence in lower case
